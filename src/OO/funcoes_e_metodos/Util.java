package POO.funcoes_e_metodos;

public class Util {
    /*Métodos estáticos.*/
    public static int somar(int a, int b){
        return a + b;
    }

    /*método estático é usado para coisas mais triviais e mais simples
    *    geralmente é aplicado no código inteiro.*/
    public static String conversorString(Integer a){
        return a.toString();
    }
}
