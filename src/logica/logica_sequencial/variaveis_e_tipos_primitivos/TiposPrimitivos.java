package logica.logica_sequencial.variaveis_e_tipos_primitivos;

public class TiposPrimitivos {
    public static void main(String[] args) {
        /*Tipos primitivos*/
       int idade; //Uma variável pode ser declarada sem ser iniciada e depois ser iniciada.
        idade = 29;

        /*Os tipos primitivos em java*/
        /*Existem 8 tipos primitivos em Java*/
        //Inteiros
        byte menor = 10;
        short meio = 15000;
        int conta = 123456;
        long maior = 121653L;
        //ponto flutuante
        double saldo = 2.500;
        float altura = 1.76f;
        //caracter (Um único cracter unicode)
        char sexo = 'F';
        //booleano (Representa verdadeiro ou falso)
        boolean transferencia = true;

    }
}
